def _shift_date(date_str: str | None, days: int) -> str | None:
    if not date_str:
        return None
    try:
        base = datetime.strptime(date_str, "%Y-%m-%d")
    except (TypeError, ValueError):
        return date_str
    return (base + timedelta(days=days)).strftime("%Y-%m-%d")


