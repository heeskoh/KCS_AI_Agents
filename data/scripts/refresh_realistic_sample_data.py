def build_company_rows(company_meta: list[dict], extracts: list[dict]) -> list[tuple]:
    """company_profiles 행(20-튜플) 생성."""
    rows = []
    for idx, (meta, ext) in enumerate(zip(company_meta, extracts), 1):
        declared_value = int(ext.get("declared_value_krw") or 0)
        annual_import = round(declared_value * meta["annual_multiplier"])
        annual_revenue = round(annual_import * 1.65)
        duty = round(annual_import * meta["duty_rate"])
        refund = round(annual_import * meta["refund_rate"])
        rows.append((
            meta["company_id"], meta["company_name"], _tax_no(idx), meta["industry_code"],
            meta["founded_year"], meta["risk_level"], meta["risk_score"], "2026-04-15",
            meta["address_postal_code"], meta["address"], meta["address_detail"],
            meta["employee_count"], meta["major_export_countries"], meta["customs_broker_firm"],
            meta["related_companies"], annual_revenue, annual_import, duty, refund,
            meta["fta_reduction_rate"],
        ))
    return rows


def build_risk_rows(company_meta: list[dict], risk_id_start: int = 2001) -> list[tuple]:
    """import_risk_scores 행(11-튜플) 생성."""
    rows = []
    for idx, meta in enumerate(company_meta, 1):
        rows.append((
            risk_id_start + idx - 1, meta["company_id"], meta["risk_level"], meta["risk_score"],
            *meta["risk"], "2026-08-05 09:00:00",
        ))
    return rows


def insert_dicts(con, table: str, cols: list[str], rows: list[dict]) -> None:
    """컬럼 명시 INSERT (positional 의존 제거)."""
    if not rows:
        return
    placeholders = ",".join(["?"] * len(cols))
    collist = ",".join(cols)
    con.executemany(
        f"INSERT INTO {table} ({collist}) VALUES ({placeholders})",
        [tuple(r.get(c) for c in cols) for r in rows],
    )


def main() -> None:
    extracts = json.loads(EXTRACT_PATH.read_text(encoding="utf-8"))
    if len(extracts) != len(COMPANY_META):
        raise RuntimeError(f"expected {len(COMPANY_META)} extracts, got {len(extracts)}")

    backup = DB_PATH.with_name(f"{DB_PATH.stem}.backup-{datetime.now():%Y%m%d-%H%M%S}{DB_PATH.suffix}")
    shutil.copy2(DB_PATH, backup)
    print(f"backup: {backup}")

    con = duckdb.connect(str(DB_PATH))
    company_ids = [row["company_id"] for row in COMPANY_META]
    placeholders = ",".join(["?"] * len(company_ids))

    # 자식 → 부모 순으로 기존 행 삭제 (이 회사들 신고분)
    con.execute(
        f"""
        DELETE FROM import_declaration_item_taxes WHERE item_id IN (
            SELECT i.item_id FROM import_declaration_items i
            JOIN import_declarations d ON i.declaration_id = d.id
            WHERE d.company_id IN ({placeholders}))
        """,
        company_ids,
    )
    con.execute(
        f"""
        DELETE FROM import_declaration_item_specs WHERE item_id IN (
            SELECT i.item_id FROM import_declaration_items i
            JOIN import_declarations d ON i.declaration_id = d.id
            WHERE d.company_id IN ({placeholders}))
        """,
        company_ids,
    )
    con.execute(
        f"""
        DELETE FROM import_declaration_items WHERE declaration_id IN (
            SELECT id FROM import_declarations WHERE company_id IN ({placeholders}))
        """,
        company_ids,
    )
    con.execute(f"DELETE FROM import_risk_scores WHERE company_id IN ({placeholders})", company_ids)
    con.execute(f"DELETE FROM import_declarations WHERE company_id IN ({placeholders})", company_ids)
    con.execute(f"DELETE FROM company_profiles WHERE company_id IN ({placeholders})", company_ids)

    company_rows = build_company_rows(COMPANY_META, extracts)
    headers, items, specs, taxes = build_declaration_rows(COMPANY_META, extracts)
    risk_rows = build_risk_rows(COMPANY_META)

    con.executemany(
        "INSERT INTO company_profiles VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)",
        company_rows,
    )
    insert_dicts(con, "import_declarations", HEADER_COLS, headers)
    insert_dicts(con, "import_declaration_items", ITEM_COLS, items)
    insert_dicts(con, "import_declaration_item_specs", SPEC_COLS, specs)
    insert_dicts(con, "import_declaration_item_taxes", TAX_COLS, taxes)
    con.executemany(
        "INSERT INTO import_risk_scores VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)",
        risk_rows,
    )

    # 비교군(SYN-*) 기업명/주소 정비 — 기존 동작 유지
    synthetic_rows = []
    existing = con.execute(
        "SELECT company_id, industry_code FROM company_profiles WHERE company_id LIKE 'SYN-%' ORDER BY company_id"
    ).fetchall()
    counters: dict[str, int] = {}
    for company_id, industry_code in existing:
        names = SYNTHETIC_NAMES.get(industry_code, ["에스앤티무역", "케이글로벌", "해온상사", "다온트레이드"])
        pos = counters.get(industry_code, 0)
        counters[industry_code] = pos + 1
        base = names[pos % len(names)]
        suffix = (pos // len(names)) + 1
        name = base if suffix == 1 else f"{base}{suffix}"
        synthetic_rows.append((name, "00000", f"{industry_code} 비교군 샘플 주소", "비교군 데이터", f"{name} 관세대행", company_id))
    con.executemany(
        """
        UPDATE company_profiles
        SET company_name = ?, address_postal_code = ?, address = ?,
            address_detail = ?, customs_broker_firm = ?
        WHERE company_id = ?
        """,
        synthetic_rows,
    )

    print(f"inserted companies: {len(company_rows)}")
    print(f"inserted declarations(header): {len(headers)}")
    print(f"inserted items: {len(items)} / specs: {len(specs)} / taxes: {len(taxes)}")
    print(f"inserted risk rows: {len(risk_rows)}")
    print(f"renamed synthetic peers: {len(synthetic_rows)}")


if __name__ == "__main__":
    main()
