            arrival_port = "인천공항(KRINC)"
            transport_type = "40 항공"
            customs_office = "040-72 인천공항세관 통관지원과"
            bl_awb = f"180-{20000000 + idx * 137:08d}"
            master_bl = f"{flight[0]}-{30000000 + idx * 211:08d}"
